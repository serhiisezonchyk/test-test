export const multiply = (num1: number, num2: number): string => {
  return `${num1}*${num2}=${num1 * num2}`;
};
export const sum = (num1: number, num2: number): string => {
  return `${num1}*${num2}=${num1 + num2}`;
};

export const diff = (num1: number, num2: number): string => {
  return `${num1}-${num2}=${num1 - num2}`;
};
